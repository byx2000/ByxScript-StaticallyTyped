#pragma once

#include <string>

enum class Opcode
{
	// 整数相关指令
	iconst,
	iload,
	istore,
	iadd,
	isub,
	imul,
	idiv,
	rem, // 取余
	icmp,
	ig,
	ige,
	il,
	ile,
	ie,
	ine,
	iand,
	ior,
	inot,
	igstore,
	igload,
	ineg,

	// 浮点数相关指令
	dconst,
	dload,
	dstore,
	dadd,
	dsub,
	dmul,
	ddiv,
	dcmp,
	dg,
	dge,
	dl,
	dle,
	de,
	dne,
	dgstore,
	dgload,
	dneg,

	// 其他指令
	nop,
	halt,
	inc,
	jmp,
	jl,
	jle,
	jg,
	jge,
	je,
	jne,
	call,
	ret,
	toi, // 强制转换成整数
	tod, // 强制转换成浮点数
	pop, // 弹出栈顶操作数
	land, // 逻辑与
	lor, // 逻辑或
	lnot, // 逻辑非
};

